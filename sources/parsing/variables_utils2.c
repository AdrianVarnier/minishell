#include "minishell.h"

int	ft_is_variable(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '$' && str[i + 1] && str[i + 1] != ' ' && str[i + 1] != '\''
		&& str[i + 1] != '\"')
		return (1);
	return (0);
}
